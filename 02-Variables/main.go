package main

import "fmt"

func main() {
	var a = 0
	println(a)

	var b = "suresh"
	println(b)

	c := 123
	println(c)

	d := "p"
	println(d)

	var e bool //default value=false
	println(e)
	e = true
	println(e)

	var f float32
	println(f)

	var g float64
	println(g)

	var h byte = 's'
	fmt.Println(h)

}
